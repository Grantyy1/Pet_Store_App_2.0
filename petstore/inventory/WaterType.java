package petstore.inventory;

/**
 * Defines the different water environments where aquatic pets can live.
 * @author Grant Peverett
 * @since March 28, 2025
 * @version 1.0 beta
 * @see <a href="https://github.com/Grantyy1/Pet_Store_App_2.0/blob/main/petstore/inventory/WaterType.java">GitHub Repository</a>
 */
public enum WaterType {
    /**
     * For fish that live in fresh water (lakes, rivers, etc.)
     */
    FRESHWATER,
    /**
     * For fish that live in salt water (oceans, seas)
     */
    SALTWATER,
    /**
     * For fish that live in mixed water (estuaries, mangroves)
     */
    BRACKISH
}
